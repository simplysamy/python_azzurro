people: list = ['Mario', 'Luigi', 'peach', 'toad']

# remove element
people.remove('peach')
print(people)

people: list = ['Mario', 'Luigi', 'peach', 'toad']
# remove element using index
people.pop(1)
print(people)

people.pop()  # remove only the last element
print(people)

#cleaer the list 
people.clear()
print(people)

people: list = ['Mario', 'Luigi', 'peach', 'toad']

# reverse the list
people.reverse()
print(people)

# sort list aphabetically
people.sort()
print(people)